public class Calculadora {

    static double suma;
    static double resta;
    static double multiplicacion;
    static double division;
    public static double suma(double num1,double num2){
        suma = (num1 + num2);
        return suma;
    }
    public static double resta(double num3,double num4){
        resta = (num3 - num4);
        return resta;
    }
    public static double multiplicacion(double num5,double num6){
        multiplicacion = (num5 * num6);
        return multiplicacion;
    }
    public static double division(double num7,double num8){
        division = (num7 / num8);
        return division;
    }
}
